int		ft_iterative_factorial(int nb)
{
	int a;

	a = 1;
	if (nb < 0 || nb > 12)
		return (0);
	else
		while (nb > 1)
		{
			a += (nb - 1) * a;
			nb--;
		}
	return (a);
}
